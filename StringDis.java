package Java_Fundamental;

import java.util.Scanner;

public class StringDis {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);		//scanner class which reads input from user
		
		System.out.println("Please enter your text here to display");		//telling to input the string 
		String s = sc.nextLine();											//storing input
		
		System.out.println(s);

	}

}
